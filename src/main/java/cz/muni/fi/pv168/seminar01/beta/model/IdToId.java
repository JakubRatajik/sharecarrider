package cz.muni.fi.pv168.seminar01.beta.model;

public class IdToId {
    private long id1;
    private long id2;

    public IdToId(long id1, long id2) {
        this.id1 = id1;
        this.id2 = id2;
    }

    public long getId1() {
        return id1;
    }

    public long getId2() {
        return id2;
    }
}
